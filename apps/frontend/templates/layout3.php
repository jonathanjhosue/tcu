 <div id="divIntroduccion"  class="div_contenido active" >

<p>

Este TCU mitiga el problema de la contaminación por mal manejo de desechos sólidos a través del consumo racional y del manejo adecuado de desechos.
</p>
<p>
Quienes matriculen este proyecto elaborarán talleres y campañas educativas para niños(as),
ofrecerán charlas sobre la importancia de seleccionar y separar los desechos sólidos, confeccionarán
módulos de manejo de desechos, construirán lombriciarios para el aprovechamiento de desechos orgánicos
y establecerán alianzas con instituciones gubernamentales y ONGs, entre otros.
</p>

<p>
 Población beneficiaria:<br/>

Centros educativos, centros de acopio y municipalidades en los cantones de Grecia, San Ramón, Poas, Sarchí, Naranjo y Palmares.
<br/>
<br/>
Ubicación:<br/>

Alajuela (Grecia).
<br/>
<br/>
Disciplinas participantes:<br/>

Laboratorista Químico, Enseñanza Del Castellano y Literatura, Enseñanza Del Ingles, Educación Primaria, Educación Preescolar, Trabajo Social, Economía Agrícola.

</p>

 </div>
