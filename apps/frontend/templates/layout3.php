        <h2>Objetivo General</h2>
<p>
Contribuir mediante la educación con el manejo adecuado de los desechos en los cantones de la Región Occidental del Valle Central
con el fin de promover el desarrollo de alternativas y herramientas para la selección, recolección y disposición final de los desechos sólidos

</p>

