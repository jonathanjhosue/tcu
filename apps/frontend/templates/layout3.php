 <ol>
     
 <li>
Colaborar en la promoción de acciones educativas y puestas en práctica con niños y niñas de educación preescolar,
primaria y secundaria orientadas al manejo adecuado de los desechos sólidos y su continuidad en el tiempo.<br>

Metas:
    <ul>
        <li>Implementación de una campaña educativa con niños, niñas y adolescentes de al menos 1 centro educativo de cada uno de los cantones involucrados en el proyecto / <br>

        </li>
        <li>Instalación de  centros de acopio permanentes en al menos cinco centro educativo donde haya participación del proyecto.<br>

        </li>
    </ul>
</li>

