<h2>Informaci&oacute;n</h2>
<p>
Un manejo adecuado de los desechos
sólidos ayuda a mantener una vida y un
ambiente saludables. <br/>
“Toda persona tiene derecho a un ambiente sano y
ecológicamente equilibrado. Por ello, está legitimada
para denunciar los actos que infrinjan ese derecho
y para reclamar la reparación del daño causado...”
Artículo 50 de la Constitución Política.
</p>

<h3>Las 5 R (erres)</h3>

<p>
    Las 5 R (erres) son las siglas de Reducir, Rechazar,
Reutilizar, Reparar y Reciclar desechos; métodos propuestos para el manejo adecuado de los desechos y, así,
evitar la contaminación del medio ambiente.

<ul class="normal">
    <li>Rechazar:	 No aceptar productos con exceso de empaques de plástico, aluminio o poroplast.</li>
    <li>Reducir:
Disminuir el volumen de los desechos.
Algunas técnicas son: comprimirlos, aprovechar la utilidad del producto al máximo,
evitar la compra de botellas no retornables.</li>
    <li>Reutilizar:	 Usar repetidamente algún recurso o ma-
terial para un fin similar o distinto, por
ejemplo rellenar los envases de vidrio. Es
lo mejor que puede hacerse con los desechos ya existentes.</li>
<li>Recuperar-Reparar:	 Todo es desechable y nada es eterno, pero
es posible alargar la vida útil de muchos
productos mediante un buen mantenimiento
y adecuada reparación.</li>
    <li>Reciclar:	 Es la transformación de los desechos en un
producto nuevo.</li>
</ul>
</p>
<p>
<h3>¿Qué es la basura?</h3>
Son los residuos que se producen como consecuen-
cia de actividades humanas (domésticas, industriales,
comerciales o de servicios) y que ya no tienen utilidad.
Los basureros causan problemas ambientales que
afectan el suelo, el agua y el aire. Con el tiempo, por
la descomposición, se producirán nuevos componen-
tes químicos que contaminarán el medio, ya que el
suelo perderá muchas de sus propiedades originales,
al igual que otros recursos importantes, como el
agua subterránea.<br>

La acumulación de basura a cielo abierto es una de
las causas principales de plagas tales como ratones,
moscas, cucarachas que transmiten enfermedades y que
afectan la salud de la población.<br>
<p>
Clasificación:
<ul>
    
Según el tipo de material de desecho:
<li>
Orgánico: es degradado por acción biológica. Lo
conforman residuos que se descomponen con el tiempo
y se integran al suelo, como los residuos de plantas y
animales.
    </li>
    <li>
Inorgánicos: formados por desechos no biodegrada-
bles, por lo que no se pueden descomponer (provenientes
de la materia inerte); pueden ser plástico, vidrio, lata,
hierro, cerámica, materiales sintéticos, metales.
    </li>
</ul>
<ul>
Según el tiempo que tardan
sus materiales en degradarse:<br>
    <li>
Los desechos biodegradables se descomponen en
forma natural, en un tiempo relativamente corto; por
    ejemplo, alimentos, plantas, animales.</li>
    <li>
Los desechos no biodegradables no se descomponen
fácilmente sino que tardan mucho tiempo en hacerlo.
Por ejemplo, el vidrio tarda unos 4.000 años, el plás-
tico tarda de 100 a 1.000 años, una lata de refresco
    tarda unos 10 años y un chicle unos cinco años</li>
</ul>
</p>
<p>
<h3>¿Qué es reciclar?</h3>
Es el proceso por el que los materiales de desperdicio
son recolectados y transformados en nuevos mate-
riales que pueden ser utilizados o vendidos como
nuevos productos o materias primas.<br>
Los objetivos del reciclaje son los siguientes:
<ul>
<li>Ahorrar energía.</li>
<li>Conservar los recursos naturales.</li>
<li>Disminuir el volumen de desechos perjudiciales.</li>
<li>Proteger el medio ambiente.</li>
<li>Disminuir la contaminación.</li>
<li>Alargar la vida de los materiales.</li>
<li>Reducir el 80% del espacio que ocupan los desperdicios al convertirse en basura.</li>
<li>Facilitar la recolección de basura.</li>
<li>Evitar la producción de los 90 millones de toneladas
	 de basura que cada persona acumula en su vida.</li>
<li>Disminuir el pago de impuestos por concepto de recolección de basura (incluido en el pago predial).</li>
<li>Vivir en un mundo más limpio.</li>
<li>Proteger nuestra salud</li>
</ul>
